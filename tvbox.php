<header>
    <h1>Projeto de Reutilização de TV Boxes Piratas</h1>
    <p>Transformando dispositivos apreendidos em computadores para estudantes carentes</p>
</header>

<section>
    <h2>Objetivo Geral</h2>
    <p>Reutilizar TV Boxes piratas apreendidos pela Polícia Federal, convertendo-os em computadores funcionais com sistema operacional Linux, destinados a estudantes de baixa renda, promovendo inclusão digital e acesso à educação.</p>
</section>

<section>
    <h2>Objetivos Específicos</h2>
    <ul>
        <li>Recuperar e higienizar os dispositivos apreendidos.</li>
        <li>Realizar a instalação de distribuições Linux leves e otimizadas.</li>
        <li>Adaptar o hardware e o software para uso educacional.</li>
        <li>Criar uma estrutura de distribuição e manutenção dos dispositivos.</li>
        <li>Capacitar voluntários e equipe técnica para suporte e manutenção.</li>
        <li>Distribuir os dispositivos a estudantes em comunidades carentes.</li>
    </ul>
</section>

<section>
    <h2>Escopo de Atividades</h2>
    <ul>
        <li>Levantamento e avaliação dos dispositivos: inspeção física, verificação de funcionamento e compatibilidade.</li>
        <li>Limpeza e reparo do hardware: substituição de componentes, limpezas internas e externas.</li>
        <li>Formatação e instalação do sistema operacional: instalação de Linux leve (ex.: Lubuntu, Xubuntu, Linux Mint).</li>
        <li>Configuração de softwares essenciais: browsers, ferramentas de educação, aplicativos de produtividade.</li>
        <li>Testes de funcionamento: garantir acessibilidade, desempenho e estabilidade.</li>
        <li>Criação de documentação técnica: manuais de uso, guias de manutenção.</li>
        <li>Capacitação de equipe: treinamento para suporte técnico e orientações para os usuários finais.</li>
        <li>Distribuição e acompanhamento: entrega aos estudantes e acompanhamento do uso.</li>
    </ul>
</section>

<section>
    <h2>Recursos Necessários</h2>
    <ul>
        <li>Hardware: TV Boxes apreendidos, componentes de reposição.</li>
        <li>Software: distribuições Linux leves, aplicativos educacionais.</li>
        <li>Equipe técnica: engenheiros, técnicos em informática, voluntários.</li>
        <li>Logística: transporte, armazenamento, pontos de distribuição.</li>
        <li>Parcerias: escolas, ONGs, instituições de ensino.</li>
    </ul>
</section>

<section>
    <h2>Cronograma</h2>
    <ul>
        <li>Mês 1-2: Levantamento, inspeção e limpeza dos dispositivos.</li>
        <li>Mês 3-4: Instalação do sistema operacional e configuração.</li>
        <li>Mês 5: Testes, documentação e capacitação.</li>
        <li>Mês 6: Distribuição e acompanhamento inicial.</li>
    </ul>
</section>

<section>
    <h2>Resultados Esperados</h2>
    <ul>
        <li>Redução do descarte de eletrônicos de forma inadequada.</li>
        <li>Inclusão digital de estudantes em situação de vulnerabilidade.</li>
        <li>Capacitação de comunidades para manutenção de dispositivos.</li>
        <li>Conscientização sobre reutilização de tecnologia.</li>
    </ul>
</section>
